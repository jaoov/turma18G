package AtividadePolimorfismoHeranca;

public class Atividade2Sons 
{
	private String cachorro;
	private String cavalo;
	private String preguica;
	private String gato;
	
	public Atividade2Sons(String cachorro, String cavalo, String preguica, String gato) {
		super();
		this.cachorro = "AUAUAUAU";
		this.cavalo = "IRRUUU";
		this.preguica = "...";
		this.gato = "MIAUU";
	}

	public String getCachorro() {
		return cachorro;
	}

	public void setCachorro(String cachorro) {
		this.cachorro = cachorro;
	}

	public String getCavalo() {
		return cavalo;
	}

	public void setCavalo(String cavalo) {
		this.cavalo = cavalo;
	}

	public String getPreguica() {
		return preguica;
	}

	public void setPreguica(String preguica) {
		this.preguica = preguica;
	}

	public String getGato() {
		return gato;
	}

	public void setGato(String gato) {
		this.gato = gato;
	}
	
	// 
	
	
	
}
